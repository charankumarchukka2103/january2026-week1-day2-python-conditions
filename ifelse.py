a=int(input("Enter the number:"))
b=int(input("Enter the number:"))
sum=a+b
if(sum>50):
    print("sum is greater than 50")
else:
    print("sum is less than 50")
    
    
    
    
    
    
#if-else statement is used to execute one block of code if a specified condition is true, and another block of code if the condition is false.
#In this example, the condition (sum>50) is checked. If it is true
#(i.e., if the sum of a and b is greater than 50), the code inside the if block is executed, and "sum is greater than 50" is printed to the console.
#If the condition is false (i.e., if the sum is less than or equal to 50), the code inside the else block is executed, and "sum is less than 50" is printed.